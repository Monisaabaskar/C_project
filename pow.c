#include <stdio.h>
#include <stdlib.h>


int power(int x, int n) 
{
    int power = 1;
    int i = 0;

    for(; i < n; i++) 
    {
        power *= x;
    }

    return power;
}



int main(int argc, char *argv[])
{
    if(argc == 3)
    {

        int x = strtol(argv[1], NULL, 10);
        int n = strtol(argv[2], NULL, 10);
        printf("power : %d\n", power(x,n));
    }
    return 0;


}






















































































































































































































































































































































